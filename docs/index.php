                                    echo '<div class="mt-4 bg-red-50 border border-red-200 text-red-700 px-4 py-3 rounded relative" role="alert">';
                                    echo 'Error en la predicción: ' . json_encode($data);
                                    echo '</div>';
                                }
                            }

